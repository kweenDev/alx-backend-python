#!/usr/bin/python3
"""
This module defines a function `add` that adds two floats.
"""


def add(a: float, b: float) -> float:
    """
    Adds two float numbers.

    Parameters:
        a (float): The first float number.
        b (float): The second float number.

    Returns:
        float: The sum of a and b.
    """
    return a + b
